package modell;

public class LadaModell {

    private String lada;
    private String szoveg;

    public LadaModell() {
        this.lada = lada;
        this.szoveg = szoveg;
    }

    public String getLada() {
        return lada;
    }

    public String getSzoveg() {
        return szoveg;
    }

    public void setSzoveg(String szoveg) {
        this.szoveg = szoveg;
    }
}
